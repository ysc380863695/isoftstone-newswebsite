async function main() {
  const db = await getDb(path.join(__dirname, '..', 'data', 'news.db'));
  initSchema(db);

  // Check current max ID to know what IDs the new articles will get
  const maxRow = queryAll('SELECT MAX(id) as maxId FROM news');
  const startId = (maxRow[0]?.maxId || 0) + 1;
  console.log(`Current max ID: ${maxRow[0]?.maxId || 'none'}, new articles will start at ${startId}`);

  for (const article of articles) {
    // Check if article already exists by source_url
    const existing = queryAll('SELECT id FROM news WHERE source_url = ?', [article.source_url]);
    if (existing.length > 0) {
      console.log(`Skipping (already exists, id=${existing[0].id}): ${article.title}`);
      continue;
    }

    db.run(
      `INSERT INTO news (title, summary, content, category, source, source_url, publish_date, cover_image, tags, key_entities, sentiment)
       VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
      [
        article.title,
        article.summary,
        article.content,
        article.category,
        article.source,
        article.source_url,
        article.publish_date,
        article.cover_image,
        article.tags,
        article.key_entities,
        article.sentiment,
      ]
    );

    const newId = queryAll('SELECT last_insert_rowid() as id');
    console.log(`Inserted article #${newId[0]?.id}: ${article.title}`);
  }

  saveDb();
  console.log('All articles inserted and saved!');

  // Verify
  const count = queryAll('SELECT COUNT(*) as cnt FROM news');
  console.log(`Total articles in DB: ${count[0]?.cnt}`);

  setTimeout(() => {
    try { closeDb(); } catch {}
    process.exit(0);
  }, 2000);
}

main().catch(e => { console.error(e); process.exit(1); });
